					if ($this->designation->Exportable) $Doc->ExportCaption($this->designation);
					if ($this->image->Exportable) $Doc->ExportCaption($this->image);
				} else {
					if ($this->id_usuario->Exportable) $Doc->ExportCaption($this->id_usuario);
					if ($this->nome->Exportable) $Doc->ExportCaption($this->nome);
					if ($this->name->Exportable) $Doc->ExportCaption($this->name);
					if ($this->gender->Exportable) $Doc->ExportCaption($this->gender);
					if ($this->mobile->Exportable) $Doc->ExportCaption($this->mobile);
					if ($this->designation->Exportable) $Doc->ExportCaption($this->designation);
					if ($this->image->Exportable) $Doc->ExportCaption($this->image);
				}
				$Doc->EndExportRow();
			}
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				if (!$Doc->ExportCustom) {
					$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
					if ($ExportPageType == "view") {
						if ($this->id_usuario->Exportable) $Doc->ExportField($this->id_usuario);
						if ($this->nome->Exportable) $Doc->ExportField($this->nome);
						if ($this->senha->Exportable) $Doc->ExportField($this->senha);
						if ($this->name->Exportable) $Doc->ExportField($this->name);
						if ($this->gender->Exportable) $Doc->ExportField($this->gender);
						if ($this->mobile->Exportable) $Doc->ExportField($this->mobile);
						if ($this->designation->Exportable) $Doc->ExportField($this->designation);
						if ($this->image->Exportable) $Doc->ExportField($this->image);
					} else {
						if ($this->id_usuario->Exportable) $Doc->ExportField($this->id_usuario);
						if ($this->nome->Exportable) $Doc->ExportField($this->nome);
						if ($this->name->Exportable) $Doc->ExportField($this->name);
						if ($this->gender->Exportable) $Doc->ExportField($this->gender);
						if ($this->mobile->Exportable) $Doc->ExportField($this->mobile);
						if ($this->designation->Exportable) $Doc->ExportField($this->designation);
						if ($this->image->Exportable) $Doc->ExportField($this->image);
					}
					$Doc->EndExportRow();
				}
			}

			// Call Row Export server event
			if ($Doc->ExportCustom)
				$this->Row_Export($Recordset->fields);
			$Recordset->MoveNext();
		}
		if (!$Doc->ExportCustom) {
			$Doc->ExportTableFooter();
		}
